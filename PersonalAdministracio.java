public class PersonalAdministració {

    private String nom;
    private String dni;
    private int edat;
    private String poblacio;
    private long sou;
    private String carreg;
    
    public PersonalAdministració(String nom, String dni, int edat, String poblacio, long sou, String carreg) {
        this.nom = nom;
        this.dni = dni;
        this.edat = edat;
        this.poblacio = poblacio;
        this.sou = sou;
        this.carreg = carreg;
    }
    
}