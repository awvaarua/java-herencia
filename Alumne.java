public class Alumne {

    private String nom;
    private String dni;
    private int edat;
    private String poblacio;
    private String curs;
    private String grup;

    public Alumne(String nom, String dni, int edat, String poblacio, String curs, String grup) {
        this.nom = nom;
        this.dni = dni;
        this.edat = edat;
        this.poblacio = poblacio;
        this.curs = curs;
        this.grup = grup;
    }
    
}